function ewiki_mime_magic(&$binary_data) {

   global $mime_magic_data;

   $fd = substr($binary_data, 0, 3072);

   foreach ($mime_magic_data as $def) {

      if ($def[0] >= $f_read) {
         continue;
      }

      $pos0 = $def[0];

      if ($pos0[0] == ">") {

         $pos0 = substr($pos0, 1);

         if (strpos($fd, $def[3], $pos0) !== false) {

               return($def[4]);
         }

      }
      else {

         $part = substr($fd, $pos0, $def[1]);

         if ($mask = $def[2]) {

            $value = 1 * ('0x'.bin2hex($part));

            if (($value & $mask) == $def[3]) {

               return($def[4]);
            }

         }
         else {

            if ($part == $def[3]) {

               return($def[4]);
            }

         }
      }

   }

}





